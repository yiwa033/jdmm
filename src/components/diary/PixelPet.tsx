/* ── 生成像素矩阵 ── */
function getPixelGrid(species: PetSpecies, mood: PetMood): ColorKey[][] {
  switch (species) {
    case 'cat': return makeCat(mood)
    case 'dog': return makeDog(mood)
    case 'rabbit': return makeRabbit(mood)
    case 'hamster': return makeHamster(mood)
    default: return makeCat(mood)
  }
}

/* ── 主组件 ── */
export default function PixelPet({ species, mood, size = 160, className = '' }: PixelPetProps) {
  const grid = getPixelGrid(species, mood)
  const colors = COLOR_MAP[species] || COLOR_MAP.cat
  const cols = 16
  const rows = 16
  const pixelSize = size / cols

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${cols} ${rows}`}
      className={className}
      style={{ imageRendering: 'pixelated' }}
      shapeRendering="crispEdges"
    >
      {grid.map((row, y) =>
        row.map((colorKey, x) => {
          if (!colorKey) return null
          const fill = colors[colorKey] || 'transparent'
          return (
            <rect
              key={`${x}-${y}`}
              x={x}
              y={y}
              width={1}
              height={1}
              fill={fill}
            />
          )
        })
      )}
    </svg>
  )
}

/* ── 导出辅助：选择器缩略图 ── */
export function PixelPetThumbnail({ species, size = 48 }: { species: PetSpecies; size?: number }) {
  return <PixelPet species={species} mood="normal" size={size} />
}
